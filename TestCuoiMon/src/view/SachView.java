    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        int row = tblSach.getSelectedRow();
        try {
            if (txtMa.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Không được để trống mã");
                return;
            }
            if (txtTen.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Không được để trống tên");
                return;
            }
            if (txtDonGia.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Không được để trống đơn giá");
                return;
            }
            double gia = Double.valueOf(txtDonGia.getText());
            if (gia < 0) {
                JOptionPane.showMessageDialog(this, "Đơn giá phải dương");
                return;
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Đơn giá phải là số");
            return;
        }
        if (row >= 0) {
            String ma = sev.getRow(row).getMa();
            Sach s = getForm();
            if (s.getMa().equals(ma)) {
                s.setMa(ma);
                sev.update(s);
                JOptionPane.showMessageDialog(this, "Update thành công");
                loadData();
            } else {
                JOptionPane.showMessageDialog(this, "Update thất bại");
            }
        } else {
            JOptionPane.showMessageDialog(this, "Chưa chọn dòng để sửa");
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

