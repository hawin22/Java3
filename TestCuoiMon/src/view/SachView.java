    void loadData() {
        model.setRowCount(0);
        for (Sach s : sev.getAll()) {
            model.addRow(new Object[]{
                s.getMa(),
                s.getTen(),
                s.getGia(),
                tam(s.getTrangThai())
            });
        }
    }

    public String tam(int tt) {
        if (tt == 0) {
            return "Hết hàng";
        } else {
            return "Còn hàng";
        }
    }

    Sach getForm() {
//        int tt = 0;
        Sach s = new Sach();
        s.setMa(txtMa.getText());
        s.setTen(txtTen.getText());
        s.setGia(Double.valueOf(txtDonGia.getText()));
        if (rdCon.isSelected()) {
//            tt = 1;
            s.setTrangThai(1);
        } else {
//            tt = 0;
            s.setTrangThai(0);
        }
        return s;
    }

    void setForm(Sach s) {
        txtMa.setText(s.getMa());
        txtTen.setText(s.getTen());
        txtDonGia.setText(s.getGia() + "");
        if (s.getTrangThai() == 1) {
            rdCon.setSelected(true);
        } else {
            rdHet.setSelected(true);
        }
    }
    void  clear(){
        txtMa.setText("");
        txtTen.setText("");
        txtDonGia.setText("");
        buttonGroup1.clearSelection();
    }

