    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        // TODO add your handling code here:
        if (txtMa.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Không được để trống mã");
            return;
        }
        if (txtTen.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Không được để trống tên");
            return;
        }
        if (txtSDT.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Không được để trống SĐT");
            return;
        }
        
            int count = 0;
            for (NhanVien nv : qlnv.getAll()) {
                if (txtMa.getText().equals(nv.getMa())) {
                    count++;
                }
            }
            if (count <= 0) {
                qlnv.addNV(getForm());
                JOptionPane.showMessageDialog(this, "Thêm thành công");
                loadData();
            } else {
                JOptionPane.showMessageDialog(this, "Trùng mã, vui lòng nhập lại");
            }
    }//GEN-LAST:event_btnThemActionPerformed

    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        // TODO add your handling code here:
                int row = tblNhanVien.getSelectedRow();
        if (txtMa.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Không được để trống mã");
            return;
        }
        if (txtTen.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Không được để trống tên");
            return;
        }
        if (txtSDT.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Không được để trống SĐT");
            return;
        }
        if (row >= 0) {
            String ma = qlnv.getRow(row).getMa();
            NhanVien nv = getForm();
            if (nv.getMa().equals(ma)) {
                nv.setMa(ma);
                qlnv.update(nv);
                JOptionPane.showMessageDialog(this, "Update thành công");
                loadData();
            } else {
                JOptionPane.showMessageDialog(this, "Update thất bại");
            }
        } else {
            JOptionPane.showMessageDialog(this, "Chưa chọn dòng để update");
        }

    }//GEN-LAST:event_btnSuaActionPerformed

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        // TODO add your handling code here:
        int row = tblNhanVien.getSelectedRow();
        if (row >= 0) {
            String ma = qlnv.getRow(row).getMa();
            qlnv.delete(ma);
            JOptionPane.showMessageDialog(this, "Xóa thành công");
            loadData();
        } else {
            JOptionPane.showMessageDialog(this, "Chưa chọn dòng để xóa");
        }
    }//GEN-LAST:event_btnXoaActionPerformed

