    void loadData() {
        model.setRowCount(0);
        for (NhanVien nv : qlnv.getAll()) {
            model.addRow(new Object[]{
                nv.getMa(),
                nv.getTen(),
                nv.getSDT(),
                nv.getTrangThai() == 0 ? "Hoạt động" : "Không hoạt động"
            });
        }
    }

    NhanVien getForm() {
        NhanVien nv = new NhanVien();
        nv.setMa(txtMa.getText());
        nv.setTen(txtTen.getText());
        nv.setSDT(txtSDT.getText());
        nv.setTrangThai(cboTrangThai.getSelectedIndex());
        return nv;
    }

    void setForm(NhanVien nv) {
        txtMa.setText(nv.getMa());
        txtTen.setText(nv.getTen());
        txtSDT.setText(nv.getSDT());
        cboTrangThai.setSelectedIndex(nv.getTrangThai());
    }

    void clear() {
        txtMa.setText("");
        txtTen.setText("");
        txtSDT.setText("");
        cboTrangThai.setSelectedIndex(-1);
    }

