    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        int row = tblXeMay.getSelectedRow();
        try {
            if (txtMA.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Không được để trống mã");
                return;
            }
            if (txtTen.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Không được để trống tên");
                return;
            }
            if (txtGiaNhap.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Không được để trống giá");
                return;
            }
            double gia = Double.valueOf(txtGiaNhap.getText());
            if (gia < 0) {
                JOptionPane.showMessageDialog(this, "Giá phải dương");
                return;
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Giá phải là 1 số");
        }
        if (row >= 0) {

            String ma = sev.getRow(row).getMa();
            XeMay xm = getForm();
            if (xm.getMa().equals(ma)) {
            xm.setMa(ma);
            sev.Update(xm);
            JOptionPane.showMessageDialog(this, "Update thành công");
            loadData();
            }
            else{
                JOptionPane.showMessageDialog(this, "UD thất bại");
            }
           

        } else {
            JOptionPane.showMessageDialog(this, "Chưa chọn dòng để update");
        }

    }//GEN-LAST:event_btnUpdateActionPerformed

