    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        // TODO add your handling code here:
        try {
            if (txtMA.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Không được để trống mã");
                return;
            }
            if (txtTen.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Không được để trống tên");
                return;
            }
            if (txtGiaNhap.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Không được để trống giá");
                return;
            }
            double gia = Double.valueOf(txtGiaNhap.getText());
            if (gia < 0) {
                JOptionPane.showMessageDialog(this, "Giá phải dương");
                return;
            }
            int count = 0;
            for (XeMay xm : sev.getAll()) {
                if (txtMA.getText().equals(xm.getMa())) {
                    count++;
                }
            }
            if (count > 0) {
                JOptionPane.showMessageDialog(this, "Trùng mã, vui lòng nhập lại");
            } else {
                sev.add(getForm());
                JOptionPane.showMessageDialog(this, "Thêm thành công");
                loadData();
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Giá phải là 1 số");
        }
    }//GEN-LAST:event_btnThemActionPerformed

