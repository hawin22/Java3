    void loadData() {
        model.setRowCount(0);
        for (XeMay xm : sev.getAll()) {
            model.addRow(new Object[]{
                xm.getMa(),
                xm.getTen(),
                xm.getGiaNhap(),
                xm.getTrangThai() == 1 ? "InActive" : "Active"
            });
        }
    }

    XeMay getForm() {
        XeMay xm = new XeMay();
        xm.setMa(txtMA.getText());
        xm.setTen(txtTen.getText());
        xm.setGiaNhap(Double.valueOf(txtGiaNhap.getText()));
        xm.setTrangThai(cboTT.getSelectedIndex());
        return xm;
    }

