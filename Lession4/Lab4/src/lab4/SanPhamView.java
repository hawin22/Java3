    void loadData(ArrayList<SanPham> list) {
        defaultTableModel = (DefaultTableModel) tblProduct.getModel();
        defaultTableModel.setRowCount(0);
        for (SanPham sanPham : list) {
            defaultTableModel.addRow(new Object[]{
                sanPham.getMa(),
                sanPham.getTen(),
                sanPham.getDonvi(),
                sanPham.getGia(),
                sanPham.getNhaCC()
            });

        }

    }

