    private void btnADDMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnADDMouseClicked
        // TODO add your handling code here:
        String maSP = txtMa.getText();
        String tenSP = txtTen.getText();
        String Dvi = cboDVi.getSelectedItem().toString();
        String donGia = txtDonGia.getText();
        String ncc = txtNhaCC.getText();

        // Kiểm tra dữ liệu
        if (maSP.isEmpty() || tenSP.isEmpty() || Dvi.isEmpty() || donGia.isEmpty() || ncc.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập đầy đủ thông tin!", "Thông báo", JOptionPane.ERROR_MESSAGE);
            return;
        }
        Integer gia = Integer.parseInt(donGia);
        SanPham sp = new SanPham(maSP, tenSP, donGia, gia, ncc);
        Boolean check = qlsp.add(sp);
        if (check) {
            JOptionPane.showMessageDialog(this, "Thêm thành công");
            loadData(qlsp.getListSP());
        }
    }//GEN-LAST:event_btnADDMouseClicked

    private void btnDeleteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnDeleteMouseClicked
        // TODO add your handling code here:
        int i = tblProduct.getSelectedRow();
        if (i<0) {
            JOptionPane.showMessageDialog(this, "Chưa chọn dòng để xóa");
        }
        Boolean check = qlsp.delete(i);
        if (check) {
            JOptionPane.showMessageDialog(this, "Xóa thành công");
            loadData(qlsp.getListSP());
        }
    }//GEN-LAST:event_btnDeleteMouseClicked

    private void tblProductMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblProductMouseClicked
        // TODO add your handling code here:
        int row = tblProduct.getSelectedRow();
        if (row < 0) {
            return;
        }
        String maSP = (String) tblProduct.getValueAt(row, 0);
        txtMa.setText(maSP);
        String tenSP = (String) tblProduct.getValueAt(row, 1);
        txtTen.setText(tenSP);
        String loai = (String) tblProduct.getValueAt(row, 2);
        cboDVi.setSelectedItem(loai);
        Integer donGia = (Integer) tblProduct.getValueAt(row, 3);
        String gia = String.valueOf(donGia);
        txtDonGia.setText(gia);
        String ncc = (String) tblProduct.getValueAt(row, 4);
        txtNhaCC.setText(ncc);
    }//GEN-LAST:event_tblProductMouseClicked

    private void btnUpDateMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnUpDateMouseClicked
        // TODO add your handling code here:
        int i = tblProduct.getSelectedRow();
        if (i<0) {
            JOptionPane.showMessageDialog(this, "Chưa chọn dòng để sửa");
        }
        String ma = txtMa.getText();
        String ten = txtTen.getText();
        String donGia = txtDonGia.getText();
        String nhaCC = txtNhaCC.getText();
        String donVi = (String) cboDVi.getSelectedItem().toString();
        Integer gia = Integer.parseInt(donGia);
        SanPham sp = new SanPham(ma, ten, donVi, gia, nhaCC);
        Boolean check = qlsp.update(i, sp);
        if (check) {
            JOptionPane.showMessageDialog(this, "Cập nhật thành công");
            loadData(qlsp.getListSP());
        }
    }//GEN-LAST:event_btnUpDateMouseClicked

