    void loadData(ArrayList<Diem> list) {
        de = (DefaultTableModel) tblDiem.getModel();
        de.setRowCount(0);
        for (Diem diem : list) {
            Object[] row = {
                diem.getMaSV(),
                diem.getTenSV(),
                diem.getDiemTA(),
                diem.getDiemTH(),
                diem.getDiemGDTC(),
                diem.getTBC()
            };
            de.addRow(row);
        }
    }

    Diem getForm() {
        String maSV = txtMA.getText();
        String tenSV = lblHoTen.getText();
        Double dta;
        Double dth;
        Double dgdtc;

        if (txtTA.getText().isEmpty()) {
            dta = 0.0;
        } else {
            dta = Double.parseDouble(txtTA.getText());
        }
        if (txtTinHoc.getText().isEmpty()) {
            dth = 0.0;
        } else {
            dth = Double.parseDouble(txtTinHoc.getText());
        }
        if (txtGDTC.getText().isEmpty()) {
            dgdtc = 0.0;
        } else {
            dgdtc = Double.parseDouble(txtGDTC.getText());
        }

        return new Diem(maSV, tenSV, dta, dth, dgdtc);
    }

    void setForm(Diem sv) {
        txtMA.setText(sv.getMaSV());
        lblHoTen.setText(sv.getTenSV());
        txtTA.setText(sv.getDiemTA() + "");
        txtTinHoc.setText(sv.getDiemTH() + "");
        txtGDTC.setText(sv.getDiemGDTC() + "");
        txtDiem.setText(sv.getTBC() + "");

    }

