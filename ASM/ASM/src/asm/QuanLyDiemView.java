    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // TODO add your handling code here:
        if (txtSearchMa.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập mã");
        } else {
            String keyWord = txtSearchMa.getText();
            ArrayList<Diem> searchMa = qld.search(keyWord);
            if (searchMa.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Không tồn tại mã sinh viên này");
                txtSearchMa.setText("");
                txtMA.setText("");
                lblHoTen.setText("");
                txtTA.setText("");
                txtTinHoc.setText("");
                txtDiem.setText("");
                txtGDTC.setText("");
            } else {
                de.setRowCount(0);
                for (Diem sv : searchMa) {
                    de.addRow(new Object[]{
                        sv.getMaSV(),
                        sv.getTenSV(),
                        sv.getDiemTA(),
                        sv.getDiemTH(),
                        sv.getDiemGDTC(),
                        sv.getTBC()
                    });
                }
            }

        }
    }//GEN-LAST:event_btnSearchActionPerformed

    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        int dem = 0;
        if (Check()) {
            for (SinhVien sv : qlsv.getAll()) {
                if (sv.getMa().equals(txtMA.getText())) {
                    dem++;
                }
            }

            if (dem == 0) {
                JOptionPane.showMessageDialog(this, "Không tồn tại mã sinh viên này");
            } else {
                String keyword = txtMA.getText();
                ArrayList<Diem> searchResults = qld.search(keyword);
                if (searchResults.isEmpty()) {
                    qld.addDiem(getForm());
                    JOptionPane.showMessageDialog(this, "Thêm thành công");
                    loadData(qld.getAll());
                } else {
                    JOptionPane.showMessageDialog(this, "Sinh viên đã tồn tại");
                }
            }
        }
    }//GEN-LAST:event_btnSaveActionPerformed

