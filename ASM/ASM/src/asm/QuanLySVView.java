    void loadData(ArrayList<SinhVien> list) {
        defaultTableModel = (DefaultTableModel) tblSinhVien.getModel();
        defaultTableModel.setRowCount(0);
        for (SinhVien sv : qlsv.getAll()) {
            Object[] row = {
                sv.getMa(),
                sv.getHoTen(),
                sv.getEmail(),
                sv.getSDT(),
                sv.getGioiTinh() ? "Nam ": "Nữ",
                sv.getDiaChi(),
                sv.getLinkAnh()
            };
            defaultTableModel.addRow(row);
        }
    }

    SinhVien getForm() {
        SinhVien sv = new SinhVien();
        sv.setMa(txtMa.getText());
        sv.setHoTen(txtHoTen.getText());
        sv.setEmail(txtEmail.getText());
        sv.setSDT(txtSDT.getText());
        sv.setDiaChi(txtDiaChi.getText());
        if (rdNam.isSelected()) {
            sv.setGioiTinh(true);
        } else {
            sv.setGioiTinh(false);
        }
        if (link == "") {
            sv.setLinkAnh("Không có ảnh");
        } else {
            sv.setLinkAnh(link);
        }
        return sv;
    }

