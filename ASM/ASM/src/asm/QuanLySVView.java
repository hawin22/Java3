    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        String ma = txtMa.getText();
        String hoTen = txtHoTen.getText();
        String email = txtEmail.getText();
        String SDT = txtSDT.getText();
        String gioiTinh = "";

        if (rdNam.isSelected()) {
            gioiTinh = "Nam";
        } else {
            gioiTinh = "Nữ";
        }
        String diaChi = txtDiaChi.getText();
        if (ma.trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Không được để trống mã");
            return;
        }
        if (hoTen.trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Không được để trống họ tên");
            return;
        }
        if (email.trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Không được để trống email");
            return;
        }
        if (SDT.trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Không được để trống số điện thoại");
            return;
        }
        if (diaChi.trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Không được để trống địa chỉ");
            return;
        }
        if (gioiTinh.trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Không được để trống giới tính");
            return;
        }
        int count = 0;
        for (SinhVien sv : qlsv.getAll()) {
            if (ma.equals(sv.getMa())) {
                count++;
            }
        }
        if (count > 0) {
            JOptionPane.showMessageDialog(this, "Trùng mã, mời nhập lại");
        } else {
            qlsv.addSV(getForm());
            JOptionPane.showMessageDialog(this, "Thêm thành công");
            loadData(qlsv.getAll());
        }
    }//GEN-LAST:event_btnSaveActionPerformed

